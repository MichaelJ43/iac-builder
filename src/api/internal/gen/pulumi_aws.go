package gen

import (
	"context"
	"fmt"
	"strings"
)

// PulumiAWSEmitter emits a minimal Pulumi (Node/TS) app using @pulumi/aws for EC2.
type PulumiAWSEmitter struct{}

func (PulumiAWSEmitter) Preview(_ context.Context, s WizardState) (map[string]string, error) {
	regions := NormalizedRegions(s)
	if len(regions) == 1 {
		return pulumiSingleRegion(s, regions[0])
	}
	return pulumiMulti(s, regions)
}

func pulumiSingleRegion(s WizardState, region string) (map[string]string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, `// Generated by iac-builder — Pulumi (TypeScript) for AWS.
// Prereq: node + npm, then: npm install && pulumi up
// (Configure AWS credentials as usual: env, profile, or OIDC.)
import * as pulumi from "@pulumi/pulumi";
import * as aws from "@pulumi/aws";

`)
	fmt.Fprintf(&b, `const inst = new aws.ec2.Instance("this", {
  ami: %s,
  instanceType: %s,
  subnetId: %s,
`, tsq(s.AMI), tsq(s.InstanceType), tsq(s.SubnetID))

	if len(s.SecurityGroupIDs) > 0 {
		fmt.Fprintf(&b, "  vpcSecurityGroupIds: %s,\n", tsStringArrayLiteral(s.SecurityGroupIDs))
	}
	if strings.TrimSpace(s.KeyName) != "" {
		fmt.Fprintf(&b, "  keyName: %s,\n", tsq(s.KeyName))
	}
	if s.AssociatePublicIP {
		fmt.Fprintf(&b, "  associatePublicIpAddress: true,\n")
	}
	if s.IMDSv2Required {
		fmt.Fprintf(&b, `  metadataOptions: {
    httpTokens: "required",
    httpPutResponseHopLimit: 1,
  },
`)
	}
	if s.EnableEbsEncryption {
		fmt.Fprintf(&b, `  rootBlockDevice: {
    encrypted: true,
    volumeType: "gp3",
  },
`)
	}
	if strings.TrimSpace(s.VPCID) != "" {
		fmt.Fprintf(&b, "  // security: expect subnet in VPC %s\n", tsq(s.VPCID))
	}
	if strings.TrimSpace(s.AppSecretsManagerSecretName) != "" || strings.TrimSpace(s.AppSSMParameterName) != "" {
		b.WriteString("  // Add secrets/SSM data sources separately; do not pass secret values in userData.\n")
	}
	b.WriteString("});\n\nexport const instanceId = inst.id;\n")

	pulumiYaml := `name: iac-builder-export
description: Pulumi (Node.js) from iac-builder
runtime: nodejs
config:
  aws:region:
    value: ` + region + `
`
	packageJSON := `{
  "name": "iac-builder-pulumi",
  "private": true,
  "dependencies": {
    "@pulumi/aws": "^6.0.0",
    "@pulumi/pulumi": "^3.0.0"
  }
}
`
	return map[string]string{
		"index.ts":     b.String(),
		"Pulumi.yaml":  pulumiYaml,
		"package.json": packageJSON,
	}, nil
}

func pulumiMulti(s WizardState, regions []string) (map[string]string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, `// Generated by iac-builder — Pulumi (TypeScript) multi-region.
// The wizard reuses the same subnet/AMI; set correct subnet/AMI for each Pulumi region before apply.
import * as pulumi from "@pulumi/pulumi";
import * as aws from "@pulumi/aws";

`)
	for i, r := range regions {
		pname := "p" + RegionProviderAlias(r)
		fmt.Fprintf(&b, `const %s = new aws.Provider("reg-%d", { region: %q });
`, pname, i, r)
	}
	b.WriteString("\n")
	firstIname := "inst" + RegionProviderAlias(regions[0])
	for i, r := range regions {
		pname := "p" + RegionProviderAlias(r)
		iname := "inst" + RegionProviderAlias(r)
		lname := "this-" + RegionFileLabel(r)
		fmt.Fprintf(&b, "const %s = new aws.ec2.Instance(%q, {\n", iname, lname)
		fmt.Fprintf(&b, `  ami: %s,
  instanceType: %s,
  subnetId: %s,
`, tsq(s.AMI), tsq(s.InstanceType), tsq(s.SubnetID))
		if len(s.SecurityGroupIDs) > 0 {
			fmt.Fprintf(&b, "  vpcSecurityGroupIds: %s,\n", tsStringArrayLiteral(s.SecurityGroupIDs))
		}
		if strings.TrimSpace(s.KeyName) != "" {
			fmt.Fprintf(&b, "  keyName: %s,\n", tsq(s.KeyName))
		}
		if s.AssociatePublicIP {
			fmt.Fprintf(&b, "  associatePublicIpAddress: true,\n")
		}
		if s.IMDSv2Required {
			fmt.Fprintf(&b, `  metadataOptions: { httpTokens: "required", httpPutResponseHopLimit: 1 },
`)
		}
		if s.EnableEbsEncryption {
			fmt.Fprintf(&b, `  rootBlockDevice: { encrypted: true, volumeType: "gp3" },
`)
		}
		fmt.Fprintf(&b, "}, { provider: %s });\n\n", pname)
		_ = i
	}
	fmt.Fprintf(&b, "export const firstInstanceId = %s.id;\n", firstIname)

	pulumiYaml := `name: iac-builder-export
description: Pulumi (Node.js) multi-region from iac-builder
runtime: nodejs
config:
  aws:region:
    value: ` + regions[0] + `
`
	packageJSON := `{
  "name": "iac-builder-pulumi",
  "private": true,
  "dependencies": {
    "@pulumi/aws": "^6.0.0",
    "@pulumi/pulumi": "^3.0.0"
  }
}
`
	return map[string]string{
		"index.ts":     b.String(),
		"Pulumi.yaml":  pulumiYaml,
		"package.json": packageJSON,
	}, nil
}
