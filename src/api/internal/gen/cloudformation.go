package gen

import (
	"context"
	"fmt"
	"strings"
)

// CloudFormationEmitter renders a minimal but valid EC2 CloudFormation template.
type CloudFormationEmitter struct{}

func (CloudFormationEmitter) Preview(_ context.Context, s WizardState) (map[string]string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, `# Generated by iac-builder (AWS CloudFormation)
# security: tighten SecurityGroupIds, prefer IMDSv2, encrypt EBS, least-privilege IAM instance profiles.
AWSTemplateFormatVersion: '2010-09-09'
Description: EC2 instance generated by iac-builder
Parameters:
  ImageId:
    Type: AWS::EC2::Image::Id
    Default: %q
  InstanceType:
    Type: String
    Default: %q
  SubnetId:
    Type: AWS::EC2::Subnet::Id
    Default: %q
`, s.AMI, s.InstanceType, s.SubnetID)

	if s.KeyName != "" {
		fmt.Fprintf(&b, `  KeyName:
    Type: String
    Default: %q
`, s.KeyName)
	}

	fmt.Fprintf(&b, `Resources:
  Ec2Instance:
    Type: AWS::EC2::Instance
    Properties:
      ImageId: !Ref ImageId
      InstanceType: !Ref InstanceType
      SubnetId: !Ref SubnetId
`)

	if len(s.SecurityGroupIDs) > 0 {
		fmt.Fprintf(&b, "      SecurityGroupIds:\n")
		for _, sg := range s.SecurityGroupIDs {
			fmt.Fprintf(&b, "        - %q\n", sg)
		}
	}
	if s.KeyName != "" {
		fmt.Fprintf(&b, "      KeyName: !Ref KeyName\n")
	}
	if s.IMDSv2Required {
		fmt.Fprintf(&b, `      MetadataOptions:
        HttpTokens: required
        HttpPutResponseHopLimit: 1
`)
	}
	if s.EnableEbsEncryption {
		fmt.Fprintf(&b, `      BlockDeviceMappings:
        - DeviceName: /dev/xvda
          Ebs:
            Encrypted: true
            VolumeType: gp3
`)
	}
	if s.AssociatePublicIP {
		fmt.Fprintf(&b, `      # Note: public IP assignment depends on subnet auto-assign public IPv4 setting.
`)
	}
	if s.VPCID != "" {
		fmt.Fprintf(&b, "      # security: expected VPC %q (ensure subnet belongs to this VPC)\n", s.VPCID)
	}

	fmt.Fprintf(&b, `Outputs:
  InstanceId:
    Value: !Ref Ec2Instance
`)
	return map[string]string{"template.yaml": b.String()}, nil
}
