package gen

import (
	"context"
	"fmt"
	"strings"
)

// CrossplaneEC2Emitter emits a Crossplane/Upbound-style Instance (ec2.aws.upbound.io) manifest.
// Requires provider-aws-ec2 in the control plane. Values map to the AWS RunInstances surface where applicable.
type CrossplaneEC2Emitter struct{}

func (CrossplaneEC2Emitter) Preview(_ context.Context, s WizardState) (map[string]string, error) {
	regions := NormalizedRegions(s)
	if len(regions) == 1 {
		return map[string]string{"ec2-instance.yaml": crossplaneInstanceYAML(s, regions[0], "iac-builder-export", "")}, nil
	}
	out := make(map[string]string, len(regions))
	for i, r := range regions {
		name := "ec2-instance.yaml"
		if i > 0 {
			name = fmt.Sprintf("ec2-instance.%s.yaml", RegionFileLabel(r))
		}
		out[name] = crossplaneInstanceYAML(s, r, fmt.Sprintf("iac-builder-export-%s", RegionFileLabel(r)), r)
	}
	return out, nil
}

func crossplaneInstanceYAML(s WizardState, region, metaName, commentRegion string) string {
	var b strings.Builder
	if commentRegion != "" {
		fmt.Fprintf(&b, "# Deploy into namespace scoped for control plane; target AWS region: %s\n", commentRegion)
	}
	fmt.Fprintf(&b, `# Generated by iac-builder — Crossplane (Upbound provider-aws EC2, v1beta1)
# Install: https://marketplace.upbound.io/providers/upbound/provider-aws-ec2
# This is a managed resource claim for an EC2 instance — reconcile with the Crossplane control plane, not with terraform.

apiVersion: ec2.aws.upbound.io/v1beta1
kind: Instance
metadata:
  name: %s
spec:
  forProvider:
    region: %s
    ami: %s
    instanceType: %s
`, yamlString(metaName), yamlString(region), yamlString(s.AMI), yamlString(s.InstanceType))
	if strings.TrimSpace(s.SubnetID) != "" {
		fmt.Fprintf(&b, "    subnetId: %s\n", yamlString(s.SubnetID))
	}
	if len(s.SecurityGroupIDs) > 0 {
		b.WriteString("    securityGroupIds:\n")
		for _, sg := range s.SecurityGroupIDs {
			fmt.Fprintf(&b, "      - %s\n", yamlString(strings.TrimSpace(sg)))
		}
	}
	if strings.TrimSpace(s.KeyName) != "" {
		fmt.Fprintf(&b, "    keyName: %s\n", yamlString(s.KeyName))
	}
	if s.AssociatePublicIP {
		b.WriteString("    # associate public IP: also ensure subnet and route table allow it\n    associatePublicIpAddress: true\n")
	}
	if s.IMDSv2Required {
		b.WriteString("    # metadata options may differ by provider version — verify API\n")
	}
	if s.EnableEbsEncryption {
		b.WriteString("    # map block device / root volume in forProvider to match your root device name (often /dev/xvda on AL2)\n")
	}
	if strings.TrimSpace(s.VPCID) != "" {
		fmt.Fprintf(&b, "    # subnet must belong to VPC: %s\n", strings.TrimSpace(s.VPCID))
	}
	if strings.TrimSpace(s.AppSecretsManagerSecretName) != "" {
		b.WriteString("    # Application secrets: use separate Kubernetes Secrets + IAM, not in-line values here.\n")
	}
	return b.String()
}

// yamlString renders a value as a double-quoted YAML string for safe use in a manifest.
func yamlString(s string) string { return fmt.Sprintf("%q", s) }
