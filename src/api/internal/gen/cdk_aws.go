package gen

import (
	"context"
	"fmt"
	"strings"
)

// CDKAWSEmitter emits a minimal AWS CDK v2 (TypeScript) stack using the L1 CfnInstance.
type CDKAWSEmitter struct{}

func (CDKAWSEmitter) Preview(_ context.Context, s WizardState) (map[string]string, error) {
	regions := NormalizedRegions(s)
	if len(regions) == 1 {
		return map[string]string{"ec2-stack.ts": cdkStack(s, "Instance", "this stack’s region (see bin/app); wizard region hint: "+regions[0])}, nil
	}
	out := make(map[string]string, len(regions))
	for i, r := range regions {
		name := "ec2-stack.ts"
		if i > 0 {
			name = fmt.Sprintf("ec2-stack.%s.ts", RegionFileLabel(r))
		}
		out[name] = cdkStack(s, fmt.Sprintf("Instance%s", RegionProviderAlias(r)), "deploy in AWS region "+r+"; set env CDK_DEFAULT_REGION or stack env")
	}
	return out, nil
}

func cdkStack(s WizardState, instanceID, note string) string {
	var b strings.Builder
	fmt.Fprintf(&b, `// Generated by iac-builder — AWS CDK v2 (TypeScript)
// %s
// In a cdk project: add this as lib/ec2-stack.ts, wire from bin/app, then: cdk synth
import { Stack, type StackProps, CfnOutput } from 'aws-cdk-lib';
import type { Construct } from 'constructs';
import { CfnInstance } from 'aws-cdk-lib/aws-ec2';

export class IacBuilderEc2Stack extends Stack {
  public readonly instance: CfnInstance;
  constructor(scope: Construct, id: string, props?: StackProps) {
    super(scope, id, props);
    this.instance = new CfnInstance(this, %q, {
      imageId: %s,
      instanceType: %s,
      subnetId: %s,
`, note, instanceID, tsq(s.AMI), tsq(s.InstanceType), tsq(s.SubnetID))

	if len(s.SecurityGroupIDs) > 0 {
		fmt.Fprintf(&b, "      securityGroupIds: %s,\n", tsStringArrayLiteral(s.SecurityGroupIDs))
	}
	if strings.TrimSpace(s.KeyName) != "" {
		fmt.Fprintf(&b, "      keyName: %s,\n", tsq(s.KeyName))
	}
	if s.AssociatePublicIP {
		b.WriteString("      // Public IP: ensure subnet is public or add a network interface; see Subnet’s MapPublicIpOnLaunch.\n")
	}
	if s.IMDSv2Required {
		b.WriteString(`      metadataOptions: { httpTokens: 'required', httpPutResponseHopLimit: 1 },
`)
	}
	if s.EnableEbsEncryption {
		b.WriteString(`      blockDeviceMappings: [
        { deviceName: '/dev/xvda', ebs: { encrypted: true, volumeType: 'gp3' } },
      ],
`)
	}
	if strings.TrimSpace(s.VPCID) != "" {
		fmt.Fprintf(&b, "      // expect subnet in VPC: %s\n", tsq(s.VPCID))
	}
	if strings.TrimSpace(s.AppSecretsManagerSecretName) != "" || strings.TrimSpace(s.AppSSMParameterName) != "" {
		b.WriteString("      // For secrets, extend stack with data sources / IAM, not userData secrets.\n")
	}
	b.WriteString(`    });
    new CfnOutput(this, 'InstanceId', { value: this.instance.ref });
  }
}
`)
	return b.String()
}
