package gen

import (
	"context"
	"fmt"
	"strings"
)

type TerraformEmitter struct{}

func (TerraformEmitter) Preview(_ context.Context, s WizardState) (map[string]string, error) {
	regions := NormalizedRegions(s)
	if len(regions) == 1 {
		return emitTerraformOneRegion(s, regions[0])
	}
	return emitTerraformMulti(s, regions)
}

func emitTerraformOneRegion(s WizardState, region string) (map[string]string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, "# Generated by iac-builder (Terraform)\n")
	fmt.Fprintf(&b, "# security: use IMDSv2 where possible; restrict SSH CIDRs; least-privilege IAM for instance profiles.\n\n")
	fmt.Fprintf(&b, `terraform {
  required_providers {
    aws = {
      source  = "hashicorp/aws"
      version = ">= 5.0"
    }
  }
}

provider "aws" {
  region = %q
}

`, region)

	smName := strings.TrimSpace(s.AppSecretsManagerSecretName)
	ssmName := strings.TrimSpace(s.AppSSMParameterName)
	if smName != "" {
		fmt.Fprintf(&b, `data "aws_secretsmanager_secret" "app_sm" {
  name = %q
}

`, smName)
	}
	if ssmName != "" {
		fmt.Fprintf(&b, `data "aws_ssm_parameter" "app_ssm" {
  name = %q
}

`, ssmName)
	}
	if smName != "" || ssmName != "" {
		fmt.Fprintf(&b, "# Least-privilege IAM on the instance role: read secrets/SSM parameters these data sources reference; do not pass secret *values* in user_data or tags.\n\n")
	}

	fmt.Fprintf(&b, `resource "aws_instance" "this" {
  ami           = %q
  instance_type = %q
  subnet_id     = %q
`, s.AMI, s.InstanceType, s.SubnetID)

	if len(s.SecurityGroupIDs) > 0 {
		fmt.Fprintf(&b, "  vpc_security_group_ids = %s\n", formatStringListHCL(s.SecurityGroupIDs))
	}
	if s.KeyName != "" {
		fmt.Fprintf(&b, "  key_name = %q\n", s.KeyName)
	}
	if s.AssociatePublicIP {
		fmt.Fprintf(&b, `  associate_public_ip_address = true
`)
	}
	if s.IMDSv2Required {
		fmt.Fprintf(&b, `  metadata_options {
    http_tokens = "required"
  }
`)
	}
	if s.EnableEbsEncryption {
		fmt.Fprintf(&b, `  root_block_device {
    encrypted   = true
    volume_type = "gp3"
  }
`)
	}
	if s.VPCID != "" {
		fmt.Fprintf(&b, "  # security: vpc_id context %q (subnet must belong to this VPC)\n", s.VPCID)
	}
	fmt.Fprintf(&b, "}\n")
	return map[string]string{"main.tf": b.String()}, nil
}

func emitTerraformMulti(s WizardState, regions []string) (map[string]string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, "# Generated by iac-builder (Terraform) — multi-region\n")
	fmt.Fprintf(&b, "# The wizard’s VPC/subnet/AMI values apply to every block; replace per region so each subnet/AMI is valid in that region. Discovery uses the first region only.\n\n")
	hclList := &strings.Builder{}
	for i, r := range regions {
		if i > 0 {
			hclList.WriteString(", ")
		}
		hclList.WriteString(fmt.Sprintf("%q", r))
	}
	fmt.Fprintf(&b, `terraform {
  required_providers {
    aws = {
      source  = "hashicorp/aws"
      version = ">= 5.0"
    }
  }
}

locals {
  target_regions = [%s]
}

`, hclList.String())

	fmt.Fprintf(&b, `provider "aws" {
  region = %q
}

`, regions[0])

	for _, r := range regions[1:] {
		fmt.Fprintf(&b, `provider "aws" {
  alias  = %q
  region = %q
}

`, RegionProviderAlias(r), r)
	}

	smName := strings.TrimSpace(s.AppSecretsManagerSecretName)
	ssmName := strings.TrimSpace(s.AppSSMParameterName)
	if smName != "" || ssmName != "" {
		for i, r := range regions {
			prov := ""
			if i > 0 {
				prov = fmt.Sprintf("  provider = aws.%s\n", RegionProviderAlias(r))
			}
			suf := RegionProviderAlias(r)
			if smName != "" {
				fmt.Fprintf(&b, `data "aws_secretsmanager_secret" "app_sm_%s" {
%s  name = %q
}

`, suf, prov, smName)
			}
			if ssmName != "" {
				fmt.Fprintf(&b, `data "aws_ssm_parameter" "app_ssm_%s" {
%s  name = %q
}

`, suf, prov, ssmName)
			}
		}
		fmt.Fprintf(&b, "# Least-privilege IAM for each instance role and regional secret/SSM in each data source.\n\n")
	}

	for i, r := range regions {
		prov := ""
		if i > 0 {
			prov = fmt.Sprintf("  provider = aws.%s\n", RegionProviderAlias(r))
		}
		suf := RegionProviderAlias(r)
		fmt.Fprintf(&b, `resource "aws_instance" "this_%s" {
%s  ami           = %q
  instance_type = %q
  subnet_id     = %q
`, suf, prov, s.AMI, s.InstanceType, s.SubnetID)
		if len(s.SecurityGroupIDs) > 0 {
			fmt.Fprintf(&b, "  vpc_security_group_ids = %s\n", formatStringListHCL(s.SecurityGroupIDs))
		}
		if s.KeyName != "" {
			fmt.Fprintf(&b, "  key_name = %q\n", s.KeyName)
		}
		if s.AssociatePublicIP {
			fmt.Fprintf(&b, `  associate_public_ip_address = true
`)
		}
		if s.IMDSv2Required {
			fmt.Fprintf(&b, `  metadata_options {
    http_tokens = "required"
  }
`)
		}
		if s.EnableEbsEncryption {
			fmt.Fprintf(&b, `  root_block_device {
    encrypted   = true
    volume_type = "gp3"
  }
`)
		}
		if s.VPCID != "" {
			fmt.Fprintf(&b, "  # security: context %q — subnet in %s\n", s.VPCID, r)
		}
		fmt.Fprintf(&b, "}\n\n")
	}
	return map[string]string{"main.tf": b.String()}, nil
}

func formatStringListHCL(ids []string) string {
	var parts []string
	for _, id := range ids {
		parts = append(parts, fmt.Sprintf("%q", id))
	}
	return "[" + strings.Join(parts, ", ") + "]"
}
