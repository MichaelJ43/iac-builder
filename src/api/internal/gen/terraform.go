package gen

import (
	"context"
	"fmt"
	"strings"
)

type TerraformEmitter struct{}

func (TerraformEmitter) Preview(_ context.Context, s WizardState) (map[string]string, error) {
	var b strings.Builder
	fmt.Fprintf(&b, "# Generated by iac-builder (Terraform)\n")
	fmt.Fprintf(&b, "# security: use IMDSv2 where possible; restrict SSH CIDRs; least-privilege IAM for instance profiles.\n\n")
	fmt.Fprintf(&b, `terraform {
  required_providers {
    aws = {
      source  = "hashicorp/aws"
      version = ">= 5.0"
    }
  }
}

provider "aws" {
  region = %q
}

`, s.Region)

	fmt.Fprintf(&b, `resource "aws_instance" "this" {
  ami           = %q
  instance_type = %q
  subnet_id     = %q
`, s.AMI, s.InstanceType, s.SubnetID)

	if len(s.SecurityGroupIDs) > 0 {
		fmt.Fprintf(&b, "  vpc_security_group_ids = %s\n", formatStringListHCL(s.SecurityGroupIDs))
	}
	if s.KeyName != "" {
		fmt.Fprintf(&b, "  key_name = %q\n", s.KeyName)
	}
	if s.AssociatePublicIP {
		fmt.Fprintf(&b, `  associate_public_ip_address = true
`)
	}
	if s.IMDSv2Required {
		fmt.Fprintf(&b, `  metadata_options {
    http_tokens = "required"
  }
`)
	}
	if s.EnableEbsEncryption {
		fmt.Fprintf(&b, `  root_block_device {
    encrypted = true
  }
`)
	}
	if s.VPCID != "" {
		fmt.Fprintf(&b, "  # security: vpc_id context %q (subnet must belong to this VPC)\n", s.VPCID)
	}
	fmt.Fprintf(&b, "}\n")
	return map[string]string{"main.tf": b.String()}, nil
}

func formatStringListHCL(ids []string) string {
	var parts []string
	for _, id := range ids {
		parts = append(parts, fmt.Sprintf("%q", id))
	}
	return "[" + strings.Join(parts, ", ") + "]"
}
